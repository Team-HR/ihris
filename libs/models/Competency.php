    /**
     * Generates report about competency
     * with definitions/descriptions from
     * given $self_assessed_scores and $sup_assessed_scores and of competency levels.
     * Returns array.
     * */

    public function gen_report($self_assessed_scores,$sup_assessed_scores)
    {
        if (!$self_assessed_scores || !$sup_assessed_scores) return null;
        $data = [];
        $competencies = $this->competencies;        

        foreach ($competencies as $index => $comp) {
            $data[$index]["name"] = $comp["name"];
            $data[$index]["description"] = $comp["description"];
            $data[$index]["self"] = $comp["levels"][$self_assessed_scores[$index]-1];
            $data[$index]["self"]["level"] = $self_assessed_scores[$index];
            $data[$index]["sup"] = $comp["levels"][$sup_assessed_scores[$index]-1];
            $data[$index]["sup"]["level"] = $sup_assessed_scores[$index];
        }
        return $data;
    }


